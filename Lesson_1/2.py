# 2. Выполнить логические побитовые операции "И", "ИЛИ" и др. над числами 5 и 6.
# Выполнить над числом 5 побитовый сдвиг вправо и влево на два знака.

num5 = 5
num6 = 6

print("Битовая операция & (И, AND): ", num5 & num6)
print("Битовый оператор | (ИЛИ, OR): ", num5 | num6)
print("Битовый оператор ^ (исключающее ИЛИ, XOR): ", num5 ^ num6)
print("Операторы сдвига влево <<: ", num5 << 2)
print("Операторы сдвига вправо >>: ", num5 >> 2)
